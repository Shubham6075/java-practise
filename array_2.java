package Placement_Course.Array;

public class array_2 {
	public static void main(String[] args) {

		// print using for loop
		int[] marks = new int[4];
		marks[0] = 34;
		marks[1] = 45;
		marks[2] = 78;
		marks[3] = 89;

		for(int i=0; i< marks.length; i++){
			System.out.println(marks[i]);
		}
	}
}
